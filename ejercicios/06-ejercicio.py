"""Escribí un programa que solicite al usuario ingresar tres números para luego mostrarle el promedio de los tres."""
num_1 = float(input("Ingresa el primer numero => "))
num_2 = float(input("Ingresa el segundo numero => "))
num_3 = float(input("Ingresa el tercer numero => "))

promedio = (((num_1 + num_2 + num_3)/3))

print(f"El promedio es => [{promedio}]")
